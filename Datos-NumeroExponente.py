# Solicitar al usuario dos números, uno para la base y otro para el exponentes, luego mostrar resultado
base = int(input("Ingrese número para la base: "))
expo = int(input("Ingrese número para el exponente: "))
print("El número",base,"elevado al exponente",expo,"es",base**expo)
print(f"El número {base}, elevado al exponente {expo} es {base**expo}")