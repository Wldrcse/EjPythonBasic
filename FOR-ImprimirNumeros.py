# Solo agregamos un parámetro, inicia en 0, incremento de 1 en uno hasta un valor inferior al declarado
for i in range (10):
    print(i)
    
# Mostrar desde el número 1 al 10
print("Otro ejemplo")
for i in range(1,11):
    print(i)
    
# Mostrar solo valores impares
print("Números impares del 1 al 9")
for i in range (1,11,2):
    print(i)